namespace Core.Utility.Web.EX
{
    //
    // 摘要:
    //     指定是否允許來自用戶端的 HTTP GET 要求。
    public enum JsonRequestBehavior
    {
        //
        // 摘要:
        //     允許來自用戶端的 HTTP GET 要求。
        AllowGet = 0,
        //
        // 摘要:
        //     不允許來自用戶端的 HTTP GET 要求。
        DenyGet = 1
    }
}
